var rodadas = function() {
	var atual = 1;
	$('.cards-side.confrontos .fa').click(function(){
		var l = $(this).parent().find('.blocks').length;
		if ($(this).is('.fa-angle-right')) {
			if (atual == l) {
				atual = 1;
			}else {
				atual++;
			}
		}else {
			if (atual == 1) {
				atual = 1;
			}else {
				atual--;
			}
		}
		$(this).parent().find('.blocks').hide();
		$(this).parent().find('.blocks:nth-child('+ atual +')').show();
	});
};

